    private void botonAbrirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAbrirArchivoActionPerformed
        JFileChooser selectorFichero = new JFileChooser();
        int result = selectorFichero.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File ficheroSeleccionado = selectorFichero.getSelectedFile();
            try {
                mostrarContenidoFichero(ficheroSeleccionado);
            } catch (IOException ex) {
                Logger.getLogger(LecturaTextoGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_botonAbrirArchivoActionPerformed

