    private void guardarContenido(File file) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            String contenido = areaTexto.getText();
            writer.write(contenido);
            JOptionPane.showMessageDialog(this, "Has guardado el archivo", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE, icono);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el archivo: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
    private void botonGuardarNuevoArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarNuevoArchivoActionPerformed
            JFileChooser selectorFichero = new JFileChooser();
            int result = selectorFichero.showSaveDialog(this);

            if (result == JFileChooser.APPROVE_OPTION) {
                File archivoGuardado = selectorFichero.getSelectedFile();
                guardarContenido(archivoGuardado);
            }
    }//GEN-LAST:event_botonGuardarNuevoArchivoActionPerformed

    private void mostrarContenidoFichero(File file) throws IOException {
        labelRutaAbsoluta.setText(file.getAbsolutePath());

        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            StringBuilder contenido = new StringBuilder(areaTexto.getText());
            int numeroDeLinea = areaTexto.getLineCount();

            String linea;
            
            while ((linea = reader.readLine()) != null) {
                contenido.append(numeroDeLinea).append(":  ").append(linea).append("\n");
                numeroDeLinea++;

            }

            areaTexto.setText(contenido.toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error leyendo fichero: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

        
